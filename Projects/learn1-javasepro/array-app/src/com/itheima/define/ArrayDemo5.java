package com.itheima.define;

public class ArrayDemo5 {
    public static void main(String[] args) {
        int[] ages = new int[3];    // 0 0 0

        System.out.println(ages[0]);
        System.out.println(ages[1]);
        System.out.println(ages[2]);

        ages[0] = 12;
        ages[1] = 20;
        ages[2] = 30;

        System.out.println(ages[0]);
        System.out.println(ages[1]);
        System.out.println(ages[2]);

    }
}
