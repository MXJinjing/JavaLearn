package com.itheima.variable;

public class VariableDemo2 {
    public static void main(String[] args) {
        //掌握基本数据类型的使用
        //1.byte short int long
        byte a = 127;

        short s = 13244;

        int i = 422424;

        long lg = 424244422224L;

        //long类型需要后面加上L

        //2.float double
        //如果需要float后面需要加上F、f
        float f = 3.14F;

        double d = 56.45;

        //3.char 字符型
        char ch = 'a';
        char ch2 = '中';

        //4.boolean
        boolean flag = true;
        boolean flag2 = false;

        //字符串类型
        String name = "张三";
    }
}
